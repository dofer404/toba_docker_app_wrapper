#!/bin/bash

CARPETAPROYECTO=`dirname $0`/../
ARCHIVO="backup_20170831"
ARCHIVO_TOBA="backupToba_20170831"
HOST=pg
PROYECTO=`cat $CARPETAPROYECTO/NOMBRE_PROYECTO`
BASEDEDATOS=`cat $CARPETAPROYECTO/NOMBRE_BASEDEDATOS`
BD_TOBA="<NOMBRE BASEDEDATOSTOBA>";

DIRBK=$CARPETAPROYECTO/bd/backup/

pg_dump -h $HOST -U postgres $BASEDEDATOS > $DIRBK/$ARCHIVO.backup
gzip $DIRBK/$ARCHIVO.backup

pg_dump -h $HOST -U postgres $BD_TOBA > $DIRBK/$ARCHIVO_TOBA.backup
gzip $DIRBK/$ARCHIVO_TOBA.backup
